func TestGenerateFullObservation(t *testing.T) {
	exitCode := 0
	obs := &Observation{
		Command:  "node server.js",
		ExitCode: 0,
		Stdout:   "Server ready on port 3000\nListening for connections",
		Stderr:   "",
		Ports: []PortBinding{
			{Port: 3000, Protocol: "tcp", Host: "0.0.0.0"},
		},
		NewFiles: []FileSnapshot{
			{Path: "dist/bundle.js", Size: 1024, Hash: "abc123"},
		},
		HTTPProbes: []HTTPProbeResult{
			{URL: "http://localhost:3000/health", StatusCode: 200, Reachable: true},
		},
	}

	out, err := Generate(obs)
	if err != nil {
		t.Fatalf("Generate() error: %v", err)
	}

	raw := string(out)

	// Verify header comment
	if !strings.HasPrefix(raw, "# Generated by:") {
		t.Error("output missing header comment")
	}
	if !strings.Contains(raw, `smokesig observe "node server.js"`) {
		t.Error("header missing command")
	}

	// Verify roundtrip through yaml.Unmarshal
	var cfg generatedConfig
	if err := unmarshalYAML(out, &cfg); err != nil {
		t.Fatalf("output is not valid YAML: %v", err)
	}

	if cfg.Version != 1 {
		t.Errorf("Version = %d, want 1", cfg.Version)
	}
	if cfg.Project != "node" {
		t.Errorf("Project = %q, want %q", cfg.Project, "node")
	}

	// Expect 5 tests: exit, stdout, port, file, http
	if len(cfg.Tests) != 5 {
		t.Fatalf("len(Tests) = %d, want 5; got: %+v", len(cfg.Tests), cfg.Tests)
	}

	// Test 1: command-exit
	assertTest(t, cfg.Tests[0], "command-exit", &exitCode, "", nil, "", nil)

	// Test 2: stdout-check
	assertTest(t, cfg.Tests[1], "stdout-check", nil, "Server ready on port 3000", nil, "", nil)

	// Test 3: port-3000
	assertTest(t, cfg.Tests[2], "port-3000", nil, "", &generatedPortCheck{Port: 3000, Protocol: "tcp"}, "", nil)

	// Test 4: file-dist-bundle-js
	assertTest(t, cfg.Tests[3], "file-dist-bundle-js", nil, "", nil, "dist/bundle.js", nil)

	// Test 5: http-health
	assertTest(t, cfg.Tests[4], "http-health", nil, "", nil, "", &generatedHTTPCheck{URL: "http://localhost:3000/health", StatusCode: intPtr(200)})
}

