// helpers

func unmarshalYAML(data []byte, cfg *generatedConfig) error {
	// Strip header comment before unmarshaling
	lines := strings.Split(string(data), "\n")
	var yamlLines []string
	for _, line := range lines {
		if !strings.HasPrefix(line, "#") {
			yamlLines = append(yamlLines, line)
		}
	}
	return yaml.Unmarshal([]byte(strings.Join(yamlLines, "\n")), cfg)
}

func yamlMarshalConfig(cfg *generatedConfig) ([]byte, error) {
	return yaml.Marshal(cfg)
}

func assertTest(t *testing.T, gt generatedTest, name string, exitCode *int, stdoutContains string, port *generatedPortCheck, fileExists string, http *generatedHTTPCheck) {
	t.Helper()
	if gt.Name != name {
		t.Errorf("test name = %q, want %q", gt.Name, name)
	}
	if exitCode != nil {
		if gt.Expect.ExitCode == nil || *gt.Expect.ExitCode != *exitCode {
			t.Errorf("exit_code mismatch: got %v, want %d", gt.Expect.ExitCode, *exitCode)
		}
	}
	if stdoutContains != "" && gt.Expect.StdoutContains != stdoutContains {
		t.Errorf("stdout_contains = %q, want %q", gt.Expect.StdoutContains, stdoutContains)
	}
	if port != nil {
		if gt.Expect.PortListening == nil {
			t.Error("port_listening is nil")
		} else {
			if gt.Expect.PortListening.Port != port.Port {
				t.Errorf("port = %d, want %d", gt.Expect.PortListening.Port, port.Port)
			}
			if gt.Expect.PortListening.Protocol != port.Protocol {
				t.Errorf("protocol = %q, want %q", gt.Expect.PortListening.Protocol, port.Protocol)
			}
		}
	}
	if fileExists != "" && gt.Expect.FileExists != fileExists {
		t.Errorf("file_exists = %q, want %q", gt.Expect.FileExists, fileExists)
	}
	if http != nil {
		if gt.Expect.HTTP == nil {
			t.Error("http is nil")
		} else {
			if gt.Expect.HTTP.URL != http.URL {
				t.Errorf("http URL = %q, want %q", gt.Expect.HTTP.URL, http.URL)
			}
			if http.StatusCode != nil {
				if gt.Expect.HTTP.StatusCode == nil || *gt.Expect.HTTP.StatusCode != *http.StatusCode {
					t.Errorf("http status_code mismatch")
				}
			}
		}
	}
}

func intPtr(v int) *int { return &v }
